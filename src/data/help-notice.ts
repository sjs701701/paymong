export function formatNoticeDate(iso: string): string {
  return iso.slice(0, 10);
}

export function formatNoticeDateTime(iso: string): string {
  if (iso.length < 16) return iso;
  return `${iso.slice(0, 10)} ${iso.slice(11, 16)}`;
}

